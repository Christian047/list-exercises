
# Write a Python program with 15 items in a list; Print the largest and smallest number from the list.


# list of 15 items

numbers = [3, 9, 2, 5, 1, 8, 7, 18, 16, 25, 34, 40, 49, 50, 60]

# max() is an in-built function for Finding  the largest number in the list

largest_number = max(numbers)

print("The largest number is:", largest_number)

# min() is an in-built function for Finding the smallest number in the list

Smallest_num=min(numbers) 

print("The smallest number is", Smallest_num)